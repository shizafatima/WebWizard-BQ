//MATH METHODS => SYNTAX => Math.method(number)
//NUMBER TO INTEGERS (4 common{1-round, 2-ceil, 3-floor, 4-trunc})
console.log("MATH METHODS:-");

console.log("Math.round(number) Returns number rounded to its nearest integer:",Math.round(5.4));
console.log("Math.ceil(number) Returns number rounded up to its nearest integer:", Math.ceil(6.7));
console.log("Math.floor(number) Returns number rounded down to its nearest integer:", Math.floor(4.6));
console.log("Math.trunc(number) Returns the integer part of number (new in ES6):", Math.trunc(7.2));







